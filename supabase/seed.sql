CREATE OR REPLACE FUNCTION "vecs"."match_documents"("namespace" "text", "result_limit" integer, "vector" "public"."vector") RETURNS TABLE("id" character varying, "similarity" double precision, "metadata" "jsonb")
    LANGUAGE "plpgsql"
    AS $_$
BEGIN
  RETURN QUERY
  EXECUTE format('
    SELECT 
      id,  -- Cast id to varchar
      vec <=> $3 AS similarity,
      metadata
    FROM vecs.%I
    ORDER BY similarity
    LIMIT $2
  ', namespace)
  USING namespace, result_limit, vector;  -- note: $3 is `vector` here
END;
$_$;


